# Быстрый вариант для одной ссылки
from urllib.parse import quote

# Ваши координаты из 2GIS (обратите внимание на порядок!)
start = (54.172976, 44.001987)  # (широта, долгота)
end = (53.184661, 44.993447)

# Самый простой URL для Google Карт
google_maps_url = f"https://www.google.com/maps/dir/{start[0]},{start[1]}/{end[0]},{end[1]}"

print(google_maps_url)
# Результат: https://www.google.com/maps/dir/53.172976,45.001987/53.184661,44.993447


