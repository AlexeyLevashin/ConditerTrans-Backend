def set_cell_text(cell, text: str) -> None:
    cell.text = text


def fill_table(table: Table, rows: list[list[str]], header_rows: int = 1) -> None:
    needed = len(rows)
    while len(table.rows) < needed:
        table.add_row()
    while len(table.rows) > needed:
        tbl = table._tbl
        tbl.remove(table.rows[-1]._tr)

    for ri, row_data in enumerate(rows):
        row = table.rows[ri]
        for ci, value in enumerate(row_data):
            if ci < len(row.cells):
                set_cell_text(row.cells[ci], value)


def insert_paragraph_after(paragraph, text: str, style: str | None = None):
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    from docx.text.paragraph import Paragraph

    p = Paragraph(new_p, paragraph._parent)
    run = p.add_run(text)
    if style:
        p.style = style
    return p


def insert_table_after(paragraph, rows: list[list[str]]) -> Table:
    doc = paragraph.part.document
    cols = len(rows[0])
    table = doc.add_table(rows=len(rows), cols=cols)
    table.style = "Table Grid"
    fill_table(table, rows)
    paragraph._p.addnext(table._tbl)
    return table


